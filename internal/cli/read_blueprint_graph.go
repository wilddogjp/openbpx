func resolveBlueprintOuterPath(asset *uasset.Asset, outerIndex uasset.PackageIndex) []string {
	out := make([]string, 0, 4)
	current := outerIndex
	guard := 0
	for current > 0 && guard < 64 {
		idx := current.ResolveIndex()
		if idx < 0 || idx >= len(asset.Exports) {
			break
		}
		exp := asset.Exports[idx]
		out = append(out, exp.ObjectName.Display(asset.Names))
		current = exp.OuterIndex
		guard++
	}
	for i, j := 0, len(out)-1; i < j; i, j = i+1, j-1 {
		out[i], out[j] = out[j], out[i]
	}
	return out
}

