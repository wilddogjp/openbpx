func runBlueprintTrace(args []string, stdout, stderr io.Writer) int {
	fs := newFlagSet("blueprint trace", stderr)
	opts := registerCommonFlags(fs)
	from := fs.String("from", "", "start node token: NodeName or NodeName.PinName")
	toNode := fs.String("to-node", "", "target node token")
	toFunction := fs.String("to-function", "", "target member token (e.g. OpenLevelBySoftObjectPtr)")
	maxDepth := fs.Int("max-depth", 128, "maximum traversal depth")
	if err := parseFlagSet(fs, args); err != nil {
		return 1
	}
	if fs.NArg() != 1 || strings.TrimSpace(*from) == "" || (strings.TrimSpace(*toNode) == "" && strings.TrimSpace(*toFunction) == "") {
		fmt.Fprintln(stderr, "usage: bpx blueprint trace <file.uasset> --from <Node|Node.Pin> [--to-node <token>] [--to-function <token>] [--max-depth <n>]")
		return 1
	}
	file := fs.Arg(0)
	asset, err := uasset.ParseFile(file, *opts)
	if err != nil {
		fmt.Fprintf(stderr, "error: %v\n", err)
		return 1
	}
	graph := buildBlueprintGraphIndex(asset)

	fromNodeToken, fromPinToken := splitNodePinToken(*from)
	startExports := findBlueprintNodeExports(graph, fromNodeToken)
	if len(startExports) == 0 {
		fmt.Fprintf(stderr, "error: no node matched --from token %q\n", fromNodeToken)
		return 1
	}
	if *maxDepth <= 0 {
		fmt.Fprintln(stderr, "error: --max-depth must be > 0")
		return 1
	}

	targetNodeToken := strings.TrimSpace(*toNode)
	targetFunctionToken := strings.TrimSpace(*toFunction)
	targetFn := func(n blueprintGraphNode) bool {
		if targetNodeToken != "" && !nodeMatchesToken(n, targetNodeToken) {
			return false
		}
		if targetFunctionToken != "" && !nodeMatchesMember(n, targetFunctionToken) {
			return false
		}
		return true
	}

	path, targetExport, found := traceBlueprintExecutionPath(graph, startExports, fromPinToken, targetFn, *maxDepth)
	pathItems := make([]map[string]any, 0, len(path))
	for i, edge := range path {
		pathItems = append(pathItems, map[string]any{
			"step":       i + 1,
			"fromExport": edge.FromExportIndex,
			"fromNode":   edge.FromNodeName,
			"fromPin":    edge.FromPin,
			"toExport":   edge.ToExportIndex,
			"toNode":     edge.ToNodeName,
			"toPin":      edge.ToPin,
		})
	}

	startNodes := make([]map[string]any, 0, len(startExports))
	for _, exportIndex := range startExports {
		node := graph.nodeForExport(exportIndex)
		if node == nil {
			continue
		}
		startNodes = append(startNodes, map[string]any{
			"export":     node.ExportIndex,
			"objectName": node.ObjectName,
			"className":  node.ClassName,
			"graphName":  node.GraphName,
		})
	}

	resp := map[string]any{
		"file":         file,
		"from":         *from,
		"toNode":       targetNodeToken,
		"toFunction":   targetFunctionToken,
		"maxDepth":     *maxDepth,
		"nodeCount":    len(graph.Nodes),
		"edgeCount":    len(graph.Edges),
		"startNodes":   startNodes,
		"found":        found,
		"pathLength":   len(pathItems),
		"path":         pathItems,
		"graphWarning": graph.Warnings,
	}
	if found {
		if node := graph.nodeForExport(targetExport); node != nil {
			resp["target"] = map[string]any{
				"export":             node.ExportIndex,
				"objectName":         node.ObjectName,
				"className":          node.ClassName,
				"graphName":          node.GraphName,
				"functionMemberName": node.FunctionMemberName,
				"eventMemberName":    node.EventMemberName,
				"customFunctionName": node.CustomFunctionName,
			}
		}
	}
	return printJSON(stdout, resp)
}

func runBlueprintCallArgs(args []string, stdout, stderr io.Writer) int {
	fs := newFlagSet("blueprint call-args", stderr)
	opts := registerCommonFlags(fs)
	member := fs.String("member", "", "function/member token")
	classFilter := fs.String("class", "K2Node_CallFunction", "class token filter")
	allPins := fs.Bool("all-pins", false, "include non-input pins")
	includeExec := fs.Bool("include-exec", false, "include exec pins")
	if err := parseFlagSet(fs, args); err != nil {
		return 1
	}
	if fs.NArg() != 1 || strings.TrimSpace(*member) == "" {
		fmt.Fprintln(stderr, "usage: bpx blueprint call-args <file.uasset> --member <token> [--class <token>] [--all-pins] [--include-exec]")
		return 1
	}

	file := fs.Arg(0)
	asset, err := uasset.ParseFile(file, *opts)
	if err != nil {
		fmt.Fprintf(stderr, "error: %v\n", err)
		return 1
	}
	graph := buildBlueprintGraphIndex(asset)

	memberToken := strings.TrimSpace(*member)
	classToken := strings.TrimSpace(*classFilter)
	matches := make([]map[string]any, 0, 16)
	for _, node := range graph.Nodes {
		if classToken != "" && !containsFold(node.ClassName, classToken) {
			continue
		}
		if !nodeMatchesMember(node, memberToken) {
			continue
		}
		pins := make([]map[string]any, 0, len(node.Pins))
		for _, pin := range node.Pins {
			if !*includeExec && isBlueprintExecPin(pin) {
				continue
			}
			if !*allPins && pin.Direction != 0 {
				continue
			}
			defaultValue, defaultSource := summarizeBlueprintPinDefault(pin)
			links := make([]string, 0, len(pin.LinkedTo))
			for _, link := range pin.LinkedTo {
				links = append(links, fmt.Sprintf("%s.%s", link.NodeName, link.PinName))
			}
			sort.Strings(links)
			pins = append(pins, map[string]any{
				"pin":           pin.Name,
				"direction":     pin.DirectionName,
				"category":      pin.Category,
				"default":       defaultValue,
				"defaultSource": defaultSource,
				"linkedCount":   len(links),
				"linkedTo":      links,
			})
		}
		matches = append(matches, map[string]any{
			"export":             node.ExportIndex,
			"objectName":         node.ObjectName,
			"className":          node.ClassName,
			"graphName":          node.GraphName,
			"functionMemberName": node.FunctionMemberName,
			"eventMemberName":    node.EventMemberName,
			"customFunctionName": node.CustomFunctionName,
			"pins":               pins,
		})
	}
	sort.Slice(matches, func(i, j int) bool {
		return asIntFromAny(matches[i]["export"]) < asIntFromAny(matches[j]["export"])
	})

	return printJSON(stdout, map[string]any{
		"file":        file,
		"member":      memberToken,
		"classFilter": classToken,
		"allPins":     *allPins,
		"includeExec": *includeExec,
		"matchCount":  len(matches),
		"matches":     matches,
		"warnings":    graph.Warnings,
	})
}

func runBlueprintRefs(args []string, stdout, stderr io.Writer) int {
	fs := newFlagSet("blueprint refs", stderr)
	opts := registerCommonFlags(fs)
	softPath := fs.String("soft-path", "", "soft object path token")
	classFilter := fs.String("class", "", "optional class token filter")
	includeRoutes := fs.Bool("include-routes", true, "include event->node routes")
	maxRoutes := fs.Int("max-routes", 3, "maximum route count per match")
	maxDepth := fs.Int("max-depth", 128, "maximum route depth")
	if err := parseFlagSet(fs, args); err != nil {
		return 1
	}
	if fs.NArg() != 1 || strings.TrimSpace(*softPath) == "" {
		fmt.Fprintln(stderr, "usage: bpx blueprint refs <file.uasset> --soft-path <path> [--class <token>] [--include-routes] [--max-routes <n>] [--max-depth <n>]")
		return 1
	}
	if *maxRoutes < 0 {
		fmt.Fprintln(stderr, "error: --max-routes must be >= 0")
		return 1
	}
	if *maxDepth <= 0 {
		fmt.Fprintln(stderr, "error: --max-depth must be > 0")
		return 1
	}

	file := fs.Arg(0)
	asset, err := uasset.ParseFile(file, *opts)
	if err != nil {
		fmt.Fprintf(stderr, "error: %v\n", err)
		return 1
	}
	graph := buildBlueprintGraphIndex(asset)
	softPathToken := strings.TrimSpace(*softPath)

	eventExports := collectBlueprintEventExports(graph)
	classToken := strings.TrimSpace(*classFilter)
	matches := make([]map[string]any, 0, 16)

	for _, node := range graph.Nodes {
		if classToken != "" && !containsFold(node.ClassName, classToken) {
			continue
		}
		for _, pin := range node.Pins {
			hitValue, source, ok := matchBlueprintSoftPathOnPin(pin, softPathToken)
			if !ok {
				continue
			}
			links := make([]string, 0, len(pin.LinkedTo))
			for _, link := range pin.LinkedTo {
				links = append(links, fmt.Sprintf("%s.%s", link.NodeName, link.PinName))
			}
			sort.Strings(links)

			routes := make([]map[string]any, 0)
			if *includeRoutes && *maxRoutes != 0 {
				for _, eventExport := range eventExports {
					if eventExport == node.ExportIndex {
						continue
					}
					path, _, found := traceBlueprintExecutionPath(graph, []int{eventExport}, "", func(n blueprintGraphNode) bool {
						return n.ExportIndex == node.ExportIndex
					}, *maxDepth)
					if !found {
						continue
					}
					eventNode := graph.nodeForExport(eventExport)
					pathItems := make([]map[string]any, 0, len(path))
					for i, edge := range path {
						pathItems = append(pathItems, map[string]any{
							"step":       i + 1,
							"fromExport": edge.FromExportIndex,
							"fromNode":   edge.FromNodeName,
							"fromPin":    edge.FromPin,
							"toExport":   edge.ToExportIndex,
							"toNode":     edge.ToNodeName,
							"toPin":      edge.ToPin,
						})
					}
					routes = append(routes, map[string]any{
						"eventExport":   eventExport,
						"eventNode":     eventNode.ObjectName,
						"eventMember":   firstNonEmpty(eventNode.EventMemberName, eventNode.CustomFunctionName),
						"pathLength":    len(pathItems),
						"executionPath": pathItems,
					})
					if *maxRoutes > 0 && len(routes) >= *maxRoutes {
						break
					}
				}
			}

			matches = append(matches, map[string]any{
				"export":      node.ExportIndex,
				"objectName":  node.ObjectName,
				"className":   node.ClassName,
				"graphName":   node.GraphName,
				"pin":         pin.Name,
				"direction":   pin.DirectionName,
				"value":       hitValue,
				"valueSource": source,
				"linkedTo":    links,
				"eventRoutes": routes,
			})
		}
	}
	return printJSON(stdout, map[string]any{
		"file":               file,
		"softPath":           softPathToken,
		"classFilter":        classToken,
		"includeRoutes":      *includeRoutes,
		"maxRoutes":          *maxRoutes,
		"maxDepth":           *maxDepth,
		"eventNodeCount":     len(eventExports),
		"matchCount":         len(matches),
		"matches":            matches,
		"graphParseWarnings": graph.Warnings,
	})
}

func runBlueprintSearch(args []string, stdout, stderr io.Writer) int {
	fs := newFlagSet("blueprint search", stderr)
	opts := registerCommonFlags(fs)
	classFilter := fs.String("class", "", "optional class token")
	memberFilter := fs.String("member", "", "optional member token")
	nameFilter := fs.String("name", "", "optional node object name token")
	show := fs.String("show", "NodePos,Function,PinDefaults", "comma-separated fields: NodePos,Function,PinDefaults,Pins,Warnings")
	limit := fs.Int("limit", 0, "optional result limit (0 = unlimited)")
	if err := parseFlagSet(fs, args); err != nil {
		return 1
	}
	if fs.NArg() != 1 {
		fmt.Fprintln(stderr, "usage: bpx blueprint search <file.uasset> [--class <token>] [--member <token>] [--name <token>] [--show <fields>] [--limit <n>]")
		return 1
	}
	if *limit < 0 {
		fmt.Fprintln(stderr, "error: --limit must be >= 0")
		return 1
	}

	showFields, err := parseBlueprintShowFields(*show)
	if err != nil {
		fmt.Fprintf(stderr, "error: %v\n", err)
		return 1
	}

	file := fs.Arg(0)
	asset, err := uasset.ParseFile(file, *opts)
	if err != nil {
		fmt.Fprintf(stderr, "error: %v\n", err)
		return 1
	}
	graph := buildBlueprintGraphIndex(asset)

	classToken := strings.TrimSpace(*classFilter)
	memberToken := strings.TrimSpace(*memberFilter)
	nameToken := strings.TrimSpace(*nameFilter)

	matches := make([]map[string]any, 0, 32)
	for _, node := range graph.Nodes {
		if classToken != "" && !containsFold(node.ClassName, classToken) {
			continue
		}
		if memberToken != "" && !nodeMatchesMember(node, memberToken) {
			continue
		}
		if nameToken != "" && !containsFold(node.ObjectName, nameToken) {
			continue
		}

		item := map[string]any{
			"export":     node.ExportIndex,
			"objectName": node.ObjectName,
			"className":  node.ClassName,
			"graphName":  node.GraphName,
			"nodeGuid":   node.NodeGUID,
		}
		for _, field := range showFields {
			switch field {
			case "NodePos":
				if node.NodePosFound {
					item["nodePos"] = map[string]any{"x": node.NodePosX, "y": node.NodePosY}
				}
			case "Function":
				item["function"] = map[string]any{
					"functionMemberName":   node.FunctionMemberName,
					"functionMemberParent": node.FunctionMemberParent,
					"eventMemberName":      node.EventMemberName,
					"eventMemberParent":    node.EventMemberParent,
					"customFunctionName":   node.CustomFunctionName,
					"eventSignatureName":   node.EventSignatureName,
					"delegatePropertyName": node.DelegatePropertyName,
				}
			case "PinDefaults":
				pinDefaults := make([]map[string]any, 0, len(node.Pins))
				for _, pin := range node.Pins {
					if isBlueprintExecPin(pin) {
						continue
					}
					defaultValue, source := summarizeBlueprintPinDefault(pin)
					links := make([]string, 0, len(pin.LinkedTo))
					for _, link := range pin.LinkedTo {
						links = append(links, fmt.Sprintf("%s.%s", link.NodeName, link.PinName))
					}
					sort.Strings(links)
					pinDefaults = append(pinDefaults, map[string]any{
						"pin":           pin.Name,
						"direction":     pin.DirectionName,
						"category":      pin.Category,
						"default":       defaultValue,
						"defaultSource": source,
						"linkedTo":      links,
					})
				}
				item["pinDefaults"] = pinDefaults
			case "Pins":
				pins := make([]map[string]any, 0, len(node.Pins))
				for _, pin := range node.Pins {
					links := make([]string, 0, len(pin.LinkedTo))
					for _, link := range pin.LinkedTo {
						links = append(links, fmt.Sprintf("%s.%s", link.NodeName, link.PinName))
					}
					sort.Strings(links)
					pins = append(pins, map[string]any{
						"pin":         pin.Name,
						"direction":   pin.DirectionName,
						"category":    pin.Category,
						"subCategory": pin.SubCategory,
						"container":   pin.ContainerType,
						"linkedTo":    links,
					})
				}
				item["pins"] = pins
			case "Warnings":
				warnings := make([]string, 0, len(node.PropertiesWarnings)+len(node.PinParseWarnings))
				warnings = append(warnings, node.PropertiesWarnings...)
				warnings = append(warnings, node.PinParseWarnings...)
				item["warnings"] = warnings
			}
		}
		matches = append(matches, item)
		if *limit > 0 && len(matches) >= *limit {
			break
		}
	}

	return printJSON(stdout, map[string]any{
		"file":         file,
		"classFilter":  classToken,
		"memberFilter": memberToken,
		"nameFilter":   nameToken,
		"show":         showFields,
		"limit":        *limit,
		"matchCount":   len(matches),
		"matches":      matches,
		"warnings":     graph.Warnings,
	})
}

func parseBlueprintShowFields(raw string) ([]string, error) {
	tokens := strings.Split(raw, ",")
	seen := map[string]bool{}
	out := make([]string, 0, len(tokens))
	for _, token := range tokens {
		norm := strings.ToLower(strings.TrimSpace(token))
		if norm == "" {
			continue
		}
		var canonical string
		switch norm {
		case "nodepos", "node_pos":
			canonical = "NodePos"
		case "function", "member":
			canonical = "Function"
		case "pindefaults", "pin_defaults":
			canonical = "PinDefaults"
		case "pins":
			canonical = "Pins"
		case "warnings":
			canonical = "Warnings"
		default:
			return nil, fmt.Errorf("unsupported --show field: %s", token)
		}
		if !seen[canonical] {
			seen[canonical] = true
			out = append(out, canonical)
		}
	}
	if len(out) == 0 {
		return nil, fmt.Errorf("--show must include at least one field")
	}
	return out, nil
}

func splitNodePinToken(raw string) (string, string) {
	raw = strings.TrimSpace(raw)
	if raw == "" {
		return "", ""
	}
	idx := strings.LastIndex(raw, ".")
	if idx <= 0 || idx >= len(raw)-1 {
		return raw, ""
	}
	return strings.TrimSpace(raw[:idx]), strings.TrimSpace(raw[idx+1:])
}

func buildBlueprintGraphIndex(asset *uasset.Asset) blueprintGraphIndex {
	graph := blueprintGraphIndex{
		Nodes:        make([]blueprintGraphNode, 0, 64),
		Edges:        make([]blueprintExecutionEdge, 0, 128),
		Warnings:     make([]string, 0, 16),
		nodeByExport: map[int]int{},
	}
	if asset == nil {
		graph.Warnings = append(graph.Warnings, "asset is nil")
		return graph
	}

	cfg := buildBlueprintGraphParseConfig(asset)
	hasOptimizedPins := hasCustomVersionAtLeast(asset, blueprintsObjectVersionGUID, blueprintVerEdGraphOptimized, true)

	for exportZeroIndex, exp := range asset.Exports {
		className := asset.ResolveClassName(exp)
		if !isBlueprintGraphNodeClass(className) {
			continue
		}

		node := blueprintGraphNode{
			ExportIndex:        exportZeroIndex + 1,
			ObjectName:         exp.ObjectName.Display(asset.Names),
			ClassName:          className,
			OuterIndex:         int32(exp.OuterIndex),
			PropertiesWarnings: make([]string, 0, 4),
			PinParseWarnings:   make([]string, 0, 4),
			decodedProperties:  map[string]any{},
		}
		node.GraphPath = resolveBlueprintOuterPath(asset, exp.OuterIndex)
		node.GraphName = firstGraphNameFromPath(node.GraphPath)

		props := asset.ParseExportProperties(exportZeroIndex)
		node.PropertiesWarnings = append(node.PropertiesWarnings, props.Warnings...)
		for _, p := range props.Properties {
			name := p.Name.Display(asset.Names)
			if v, ok := asset.DecodePropertyValue(p); ok {
				enrichDecodedTextValue(v)
				node.decodedProperties[name] = v
			}
		}
		readBlueprintNodeMetadata(&node)

		if hasOptimizedPins {
			pins := parseBlueprintNodePins(asset, exportZeroIndex, props.EndOffset, cfg)
			node.Pins = pins.Pins
			node.PinParseWarnings = append(node.PinParseWarnings, pins.Warnings...)
			node.PinParseStartOffset = pins.Start
			node.PinParseConsumedBytes = pins.Consumed
			node.pinParseConfig = pins.ConfigUsed
		} else {
			node.PinParseWarnings = append(node.PinParseWarnings, "pin parsing skipped: package custom version predates EdGraphPinOptimized")
		}

		graph.nodeByExport[node.ExportIndex] = len(graph.Nodes)
		graph.Nodes = append(graph.Nodes, node)
	}

	resolveBlueprintGraphLinks(&graph)
	buildBlueprintExecutionEdges(&graph)
	for _, node := range graph.Nodes {
		for _, warning := range node.PinParseWarnings {
			graph.Warnings = append(graph.Warnings, fmt.Sprintf("export %d (%s): %s", node.ExportIndex, node.ObjectName, warning))
		}
	}

	sort.Slice(graph.Nodes, func(i, j int) bool { return graph.Nodes[i].ExportIndex < graph.Nodes[j].ExportIndex })
	graph.nodeByExport = map[int]int{}
	for i := range graph.Nodes {
		graph.nodeByExport[graph.Nodes[i].ExportIndex] = i
	}
	sort.Slice(graph.Edges, func(i, j int) bool {
		a := graph.Edges[i]
		b := graph.Edges[j]
		if a.FromExportIndex != b.FromExportIndex {
			return a.FromExportIndex < b.FromExportIndex
		}
		if a.FromPin != b.FromPin {
			return a.FromPin < b.FromPin
		}
		if a.ToExportIndex != b.ToExportIndex {
			return a.ToExportIndex < b.ToExportIndex
		}
		return a.ToPin < b.ToPin
	})

	return graph
}

func (g blueprintGraphIndex) nodeForExport(exportIndex int) *blueprintGraphNode {
	if idx, ok := g.nodeByExport[exportIndex]; ok {
		return &g.Nodes[idx]
	}
	return nil
}

func traceBlueprintExecutionPath(
	graph blueprintGraphIndex,
	startExports []int,
	startPinToken string,
	targetFn func(blueprintGraphNode) bool,
	maxDepth int,
) ([]blueprintExecutionEdge, int, bool) {
	if maxDepth <= 0 || len(graph.Nodes) == 0 || len(startExports) == 0 {
		return nil, 0, false
	}
	adj := map[int][]blueprintExecutionEdge{}
	for _, edge := range graph.Edges {
		adj[edge.FromExportIndex] = append(adj[edge.FromExportIndex], edge)
	}

	type state struct {
		Export int
		Path   []blueprintExecutionEdge
	}
	queue := make([]state, 0, len(startExports))
	visitedDepth := map[int]int{}
	for _, exportIndex := range startExports {
		queue = append(queue, state{Export: exportIndex, Path: nil})
		visitedDepth[exportIndex] = 0
	}

	for len(queue) > 0 {
		current := queue[0]
		queue = queue[1:]
		node := graph.nodeForExport(current.Export)
		if node != nil && targetFn(*node) {
			return current.Path, current.Export, true
		}
		if len(current.Path) >= maxDepth {
			continue
		}
		for _, edge := range adj[current.Export] {
			if len(current.Path) == 0 && startPinToken != "" && !strings.EqualFold(edge.FromPin, startPinToken) {
				continue
			}
			nextDepth := len(current.Path) + 1
			if prevDepth, ok := visitedDepth[edge.ToExportIndex]; ok && prevDepth <= nextDepth {
				continue
			}
			visitedDepth[edge.ToExportIndex] = nextDepth
			nextPath := make([]blueprintExecutionEdge, 0, len(current.Path)+1)
			nextPath = append(nextPath, current.Path...)
			nextPath = append(nextPath, edge)
			queue = append(queue, state{Export: edge.ToExportIndex, Path: nextPath})
		}
	}
	return nil, 0, false
}

func findBlueprintNodeExports(graph blueprintGraphIndex, token string) []int {
	token = strings.TrimSpace(token)
	if token == "" {
		return nil
	}
	// Prefer exact match first.
	exact := make([]int, 0, 4)
	for _, node := range graph.Nodes {
		if nodeMatchesTokenExact(node, token) {
			exact = append(exact, node.ExportIndex)
		}
	}
	if len(exact) > 0 {
		sort.Ints(exact)
		return exact
	}
	contains := make([]int, 0, 8)
	for _, node := range graph.Nodes {
		if nodeMatchesToken(node, token) {
			contains = append(contains, node.ExportIndex)
		}
	}
	sort.Ints(contains)
	return contains
}

func nodeMatchesTokenExact(node blueprintGraphNode, token string) bool {
	if strings.EqualFold(node.ObjectName, token) {
		return true
	}
	for _, alias := range nodeAliases(node) {
		if strings.EqualFold(alias, token) {
			return true
		}
	}
	return false
}

func nodeMatchesToken(node blueprintGraphNode, token string) bool {
	if containsFold(node.ObjectName, token) {
		return true
	}
	for _, alias := range nodeAliases(node) {
		if containsFold(alias, token) {
			return true
		}
	}
	return false
}

func nodeMatchesMember(node blueprintGraphNode, token string) bool {
	if token == "" {
		return true
	}
	for _, v := range []string{
		node.FunctionMemberName,
		node.EventMemberName,
		node.CustomFunctionName,
		node.EventSignatureName,
		node.DelegatePropertyName,
	} {
		if containsFold(v, token) {
			return true
		}
	}
	return false
}

func nodeAliases(node blueprintGraphNode) []string {
	out := make([]string, 0, 6)
	appendAlias := func(v string) {
		v = strings.TrimSpace(v)
		if v == "" {
			return
		}
		for _, existing := range out {
			if strings.EqualFold(existing, v) {
				return
			}
		}
		out = append(out, v)
	}
	appendAlias(node.FunctionMemberName)
	appendAlias(node.EventMemberName)
	appendAlias(node.CustomFunctionName)
	appendAlias(node.EventSignatureName)
	appendAlias(node.DelegatePropertyName)
	return out
}

